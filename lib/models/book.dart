class Book {
  final String title;
  final String author;
  final double rating;
  final int progress;
  final String status;
  final String image;
  final String category;
  final int year;
  final int pages;
  final String language;
  final String description;

  Book({
    required this.title,
    required this.author,
    required this.rating,
    required this.progress,
    required this.status,
    required this.image,
    required this.category,
    required this.year,
    required this.pages,
    required this.language,
    required this.description,
  });
}
